export function getCalculatedFallbackInventory() {
    return FALLBACK_INVENTORY.map(p => {
        const prices = p.variants?.map((v: any) => v.price) || []
        const minP = prices.length > 0 ? Math.min(...prices) : 0
        return { ...p, startingPrice: minP }
    })
}